/*
(Divisible by 2 or 3) Find the first 10 numbers with 50 decimal digits that are
divisible by 2 or 3.
 */
package chapter10.oothinking.exercises;

import java.math.BigInteger;

public class Exercise_10_16 {
    public static void main(String[] args) {

        BigInteger value = BigInteger.TEN.pow(50);
        int counter = 0;
        while (counter < 50) {
            value = value.add(BigInteger.ONE);
            if (value.mod(BigInteger.TWO) == BigInteger.ZERO || value.mod(BigInteger.valueOf(3)) == BigInteger.ZERO) {
                System.out.println(value);
                counter++;
            }
        }
    }
}

