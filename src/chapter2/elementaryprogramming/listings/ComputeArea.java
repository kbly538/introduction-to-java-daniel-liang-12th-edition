package chapter2.elementaryprogramming.listings;

public class ComputeArea {

	public static void main(String[] args) {
		
		double radius, area;
		radius = 20;
		area = radius * radius * Math.PI;
		
		System.out.println("The area for circle of radius " + radius + " is " + area);
		
	}

}
